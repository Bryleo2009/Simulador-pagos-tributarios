     void CargarDepar()
    {
        int codigoDepar=0;
        cbDepar.removeAllItems();
        for(int i=0;i<u.getTamaño();i++)
        {
            if(codigoDepar!=u.obtener(i).getCodigoDepar_Depar())
            {
                cbDepar.addItem(u.obtener(i).getDepar().toUpperCase());
                codigoDepar=u.obtener(i).getCodigoDepar_Depar();
            }
                
        }
        cbDepar.setSelectedIndex(-1);
    }
