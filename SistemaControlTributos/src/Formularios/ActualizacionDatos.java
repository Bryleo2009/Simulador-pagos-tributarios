    void AsignacionValores()
    {
        txtDniCli.setText(Registro.unregistro.getUsuario().getDNI());
        txtNombreCli.setText(Registro.unregistro.getUsuario().getNombre());
        int día = Registro.unregistro.getUsuario().getFechaNacimiento().getDia();
        int mes = Registro.unregistro.getUsuario().getFechaNacimiento().getMes();
        int anio = Registro.unregistro.getUsuario().getFechaNacimiento().getAnio();
        Date fechaNacimiento = new Date();
        fechaNacimiento.setDate(día);
        fechaNacimiento.setMonth(mes);
        fechaNacimiento.setYear(anio);
        dataFechaCli.setDate(fechaNacimiento);
        if(Registro.unregistro.getUsuario().getSexo().equals("Masculino"))
        {
            rbMasCli.setSelected(true);
        }
        else
        {
            rbFemCli.setSelected(true);
        }
        
        txtVia.setText(Registro.unregistro.getUsuario().getDomicilio().getTipo_NombreVia());
        txtZona.setText(Registro.unregistro.getUsuario().getDomicilio().getTipo_NombreZona());
        txtNumero.setText(String.valueOf(Registro.unregistro.getUsuario().getDomicilio().getNumero()));
        txtMz.setText(Registro.unregistro.getUsuario().getDomicilio().getManzana());
        txtLote.setText(Registro.unregistro.getUsuario().getDomicilio().getLote());
        txtComercial.setText(Registro.unregistro.getLoborales().getNombreComercial());
        txtActividad.setText(Registro.unregistro.getLoborales().getActividadEconomica());
        txtOficio.setText(Registro.unregistro.getLoborales().getOficio());
        txtTelefono.setText(Registro.unregistro.getLoborales().getTelefono());
        txtCelular.setText(Registro.unregistro.getLoborales().getCelular());
        txtCorreo.setText(Registro.unregistro.getLoborales().getCorreo());
        CargarDepar();
        cbDepar.setSelectedItem(Registro.unregistro.getUsuario().getDomicilio().getDepartamento());
        cbProvin.setSelectedItem(Registro.unregistro.getUsuario().getDomicilio().getProvincia());
        cbDistrito.setSelectedItem(Registro.unregistro.getUsuario().getDomicilio().getDistrito());
        
    }
