    void Modificador(){
        String dni=Registro.unregistro.getUsuario().getDNI();
        Registro e=a.buscar(dni);
        Direccion unDomicilio = new Direccion(obtenerDepar(), obtenerProvin(), obtenerDistrito(), ObtenerZona(), ObtenerVia(), ObtenerManzana(), ObtenerLote(),ObtenerNumero());
        Fecha unaFecha = new Fecha(ObtenerDiaCliente(), ObtenerMesCliente(), ObtenerAnioCliente());
        Usuario unUsuario = new Usuario(unDomicilio, ObtenerNombre(), ObtenerDni(), ObtenerSexo(), unaFecha);
        DatosLaborales unLabor = new DatosLaborales(ObtenerComercial(), ObtenerActividad(), ObtenerOficio(), ObtenerTelefono(), ObtenerCelular(), ObtenerCorreo());
        unUsuario.setFechaNacimiento(unaFecha);
        unUsuario.setDomicilio(unDomicilio);
        Registro.unregistro.setUsuario(unUsuario); //esta es una variable temporal que se le asigna valores al iniciar sesión y lo uso para mostrar datos específicos
        Registro.unregistro.setLoborales(unLabor);
        e.setUsuario(unUsuario);
        e.setLoborales(unLabor);
        a.grabar();
       
    }
