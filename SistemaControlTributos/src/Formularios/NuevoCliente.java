    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        if(obtenerDepar().equals("-")||obtenerDistrito().equals("-")||obtenerProvin().equals("-")||ObtenerDni().equals("-")||ObtenerNombre().equals("-"))
        {
            JOptionPane.showMessageDialog(null,"Para guardar un usuario, es necesario que NOMBRE, DNI, DEPARTAMENTO, PROVINCIA & DISTRITO contengan datos validos");
        }
        else
        {
            Registro x=a.buscar(ObtenerDni());

            if (x==null){//Si no encontramos DNI repetido ingresa al if

                Direccion unDomicilio = new Direccion(obtenerDepar(), obtenerProvin(),obtenerDistrito(),ObtenerZona(),ObtenerVia(), ObtenerManzana(),ObtenerLote(),ObtenerNumero());
                Fecha unaFecha = new Fecha(ObtenerDiaCliente(),ObtenerMesCliente(),ObtenerAnioCliente());
                Usuario unUsuario = new Usuario(unDomicilio,ObtenerNombre(),ObtenerDni(),ObtenerSexo(), unaFecha);
                DatosLaborales unLabor = new DatosLaborales(ObtenerComercial(),ObtenerActividad(),ObtenerOficio(),ObtenerTelefono(),ObtenerCelular(),ObtenerCorreo());
                Fecha unaFechaRegistro = new Fecha(ObtenerDiaRegistro(),ObtenerMesRegistro(),ObtenerAnioRegistro());
                x=new Registro(unUsuario,ObtenerCategoria(),unaFechaRegistro,unLabor);

                //Asosiación

                unUsuario.setFechaNacimiento(unaFecha);
                unUsuario.setDomicilio(unDomicilio);

                x.setFechaRegistro(unaFechaRegistro);
                x.setLoborales(unLabor);
                x.setUsuario(unUsuario);

                a.adicionar(x);
                a.grabar();

                 //mostreo de datos
                JOptionPane.showMessageDialog(null, "Por favor recordar su cambio posteriormente", "CLAVE SOL GENERADA",1);
                txtRUC.setText(x.getRUC());
                txtClaveRUC.setText(x.getClaveSOL());
                txtDniRUC.setText(x.getUsuario().getDNI());

                //ocultar comandos
                VentanaRegistro.setVisible(false);

                btnGuardar.setVisible(false);
                DatosRUC.setVisible(true);
            }else JOptionPane.showMessageDialog(this,"DNI ya Registrado");
        }
   
 
    }//GEN-LAST:event_btnGuardarActionPerformed

