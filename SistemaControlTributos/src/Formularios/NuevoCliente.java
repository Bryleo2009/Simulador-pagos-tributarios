    void LimitadorNum(JTextField objeto,int limite) //limita los caracteres y su tamañao de los txt
    {
        RestrictedTextField obj = new RestrictedTextField (objeto); //elemento al cual voy a limitar
        obj.setLimit(limite); //limite de caracteres
        
        obj.setOnlyNums(true);//para que solo acepte números
        //obj.setOnlyText(true);//para que solo me acepte letras
    }
    void LimitadorText(JTextField objeto,int limite) //limita los caracteres y su tamañao de los txt
    {
        RestrictedTextField obj = new RestrictedTextField (objeto); //elemento al cual voy a limitar
        obj.setLimit(limite); //limite de caracteres
        //obj.setOnlyNums(true);//para que solo acepte números
        obj.setOnlyText(true);//para que solo me acepte letras
        obj.setAcceptSpace(true); //acepta espacios
    }
    void LimitadorAlfanum(JTextField objeto,int limite) //limita los caracteres y su tamañao de los txt
    {
        RestrictedTextField obj = new RestrictedTextField (objeto); //elemento al cual voy a limitar
        obj.setLimit(limite); //limite de caracteres
        obj.setOnlyAlphaNumeric(true);
    }
    void LimitadorCorreo(JTextField objeto,int limite)
    {
        RestrictedTextField obj = new RestrictedTextField (objeto); //elemento al cual voy a limitar
        obj.setLimit(limite); //limite de caracteres
        obj.setAcceptCharacters(".0123456789abcdefghijklmnñopqrstuvwxyz-_@");
        obj.setOnlyCustomCharacters(true);
        
    }
